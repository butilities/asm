INCLUDE IO\IO.ASM

STACK SEGMENT STACK
	DB 328 DUP(?)
STACK ENDS

CODE SEGMENT
	ASSUME CS:CODE, SS:STACK
START:	MOV SI, SP
	MOV AL, 'Y'
	OUTCH '>'
	OUTCH ' '
INPUT:	INCH BL
	CMP BL, '.'
	JE ENDTXT
	CMP BL, '('
	JE OPEN
	JNE NOOPEN
OPEN:	PUSH BX
	JMP INPUT
NOOPEN: MOV BH, '('
	CMP BL, ')'
	JE CLOSE
	MOV BH, '['
	CMP BL, ']'
	JNE INPUT
CLOSE:	CMP SP, SI
	JE NO
	POP CX
	CMP CL, BH
	JE INPUT
	JMP NO
ENDTXT:	CMP SP, SI
	JE OUTPUT
NO:	MOV SP, SI
	MOV AL, 'N'
OUTPUT:	OUTCH AL
	FINISH
CODE ENDS
END START
