INCLUDE IO\io.asm

STACK SEGMENT STACK
	DB 128 DUP (?)
STACK ENDS
DATA SEGMENT
	N EQU 10
	X DB N DUP (?), '$'
DATA ENDS

CODE SEGMENT
	ASSUME SS:STACK, DS:DATA, CS:CODE
START:	MOV AX, DATA
	MOV DS, AX

	OUTCH ' '
	OUTCH ' '

        MOV CX, N
L:	OUTCH '.'
	LOOP L
	NEWLINE

	OUTCH '>'
	OUTCH ' '

	MOV CX, N
	MOV SI, N-1
LL:	INCH X[SI]
	DEC SI
	LOOP LL

        OUTCH ' '
        OUTCH ' '

	LEA DX, X
	OUTSTR

	FINISH
CODE ENDS
END START
