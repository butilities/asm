INCLUDE IO\io.asm

STACK SEGMENT STACK
	DW 64 DUP (?)
STACK ENDS

IF0 MACRO X, L
	LOCAL NOT_EQUAL
	IF TYPE X NE DWORD
		CMP X, 0
		JE L
	ELSE
		CMP WORD PTR X, 0
		JNE NOT_EQUAL
		CMP WORD PTR X+2, 0
		JE L
	NOT_EQUAL:
	ENDIF
ENDM

CODE SEGMENT
	ASSUME SS:STACK, CS:CODE
START:
	B DB 1
	D DD 1
	IF0 B, L1
	IF0 D, L2

	FINISH
CODE ENDS
END START
