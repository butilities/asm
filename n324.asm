INCLUDE IO\io.asm

STACK SEGMENT STACK
	DB 128 DUP (?)
STACK ENDS

CODE SEGMENT
	ASSUME SS:STACK, CS:CODE
START:	OUTCH '>'
	OUTCH ' '

	INCH BL
	SUB BL, '0'
	MOV BH, 0
LL:	INCH AL
	CMP AL, '.'
	JE TOOUT
	INCH AH
	SUB AH, '0'
	CMP AL, '+'
	JE TOADD
	CMP AL, '-'
	JE TOSUB
TOADD:	ADD BL, AH
	ADC BH, 0
	JMP LL
TOSUB:	SUB BL, AH
	SBB BH, 0
	JMP LL

TOOUT:	OUTCH ' '
	OUTCH '='
	OUTCH ' '
	OUTINT BX

	FINISH
CODE ENDS
END START
