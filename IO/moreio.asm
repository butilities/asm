; Output string to symbol END_SYMBOL
; DS:DX - address of begin string
OUTSTR_MORE MACRO END_SYMBOL
	LOCAL LOOPER, TOQUIT
	PUSH DX
	PUSH AX
	PUSH BX

	MOV BX, DX
	MOV AH, 2

LOOPER:	MOV DL, [BX]
	CMP DL, END_SYMBOL
	JE TOQUIT
	INT 21h
	INC BX
	JMP LOOPER

TOQUIT:	POP BX
	POP AX
	POP DX
	ENDM

; Output register BX as bitmap
; BX - bitmap
PRINT_BITMAP MACRO COUNT
	LOCAL LOOPER
	PUSH DX
	PUSH AX
	PUSH BX
	PUSH CX

	MOV AH, 2
	MOV CX, COUNT

LOOPER:	MOV DL, 0
	SHL BX, 1
	ADC DL, '0'
	INT 21h
	LOOP LOOPER

	POP CX
	POP BX
	POP AX
	POP DX
	ENDM
